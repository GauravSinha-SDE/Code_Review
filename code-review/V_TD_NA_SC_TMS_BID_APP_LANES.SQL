create or replace view SC_LOG.INT_TMS.V_TD_NA_SC_TMS_BID_APP_LANES(
	LANE COMMENT 'Concatenation Of Origin And Destination Zone Code',
    LANE_ASSOCIATION_KEY COMMENT 'Lane Association Key',
	ORIGIN_GROUP COMMENT 'Origin Grouped Into ALL Romeoville (KCP/Skincare/NOF) |Or | Appleton/Kaukauna/Little Chute/Menasha | Or | Beech Island, SC/Graniteville, SC/Augusta, GA | Or | Milwaukee/Cudahy/Oak Creek/West Allis | Or | Green Bay/De Pere/Hobart,',
    ORIGIN_COUNTRY COMMENT 'The Country Code Of The Origin Zone.',
    ORIGIN_CITY_STATE COMMENT 'Standardized Code For The Origin Geographical Zone',
    ORIGIN COMMENT 'The City Name Of The Origin / Also Origin City',
	ORIGIN_ZIP COMMENT 'The Postal Code Of The Origin.',
	DESTINATION_COUNTRY COMMENT 'The Country Code Of The Destination Zone.',
	DESTINATION_CITY_STATE COMMENT 'The Destination Zone Code Representing State And Zip.',
	DESTINATION COMMENT 'The City/State/Zip Of The Destination.',
    COMMODITY COMMENT 'Commodity',
    EQUIPMENT COMMENT 'Equipment',
	TOTAL_MILES COMMENT 'The Distance In Miles For The Lane',
    BID_LOADS COMMENT 'Bid Loads',
	UPDATED_LOADS COMMENT 'Calculated From (Lane_Availability.Max_Qty_Constraint * 52) At Lane Level',
    HISTORICAL_LOADS COMMENT 'Historical Loads',
	FMIC COMMENT 'Fmic',
    COMMENTS COMMENT 'Comments',
	PRIMARY_CUSTOMER COMMENT 'Concat Of Top 5 Customers In Lst 6 Months',
    PRIMARY_UNLOAD_TYPE COMMENT 'Primary Unload Type',
	ORDER_TYPE COMMENT 'Order Type',
	EFFECTIVE_DATE COMMENT 'The Effective Date For The Lane.',
	EXPIRATION_DATE COMMENT 'The Expiration Date For The Lane.',
    PRE_AWARDED_LANE COMMENT 'Pre Awarded Lane',
	AAO COMMENT 'Aao (Chrg_Cd)',
    COSTCO_GROUP COMMENT 'CostcoGroup',
	AWARD_WEIGHTED_RPM COMMENT 'Need Bar | And Refresh  Daily And Update ',
    AWARD_WEIGHTED_SERVICE COMMENT 'Award Weighted Service From BAR',
    COUPA_TAB COMMENT 'Coupa Tab',
    BUSINESS_UNIT COMMENT 'Business Unit',
	CURRENT_VOLUME COMMENT 'Count(Ld_Leg_Id) With Dates In Between Rate_Effective_Date And Rate_Expiration_Date',
	DESTINATION_ZIP COMMENT 'Destination City State without US_5% or 5%',
    LTA COMMENT 'LTA',
	CC_SOURCE_SYSTEM COMMENT 'Source System',
	CC_CREATED_DATETIMESTAMP COMMENT 'Record Created Timestamp',
	CC_UPDATED_DATETIMESTAMP COMMENT 'Record Updated Timestamp',
	CC_TIMEZONE COMMENT 'Time Zone'
) COMMENT='Bid Apps Lane view'
 as(
    SELECT 
        LANE,
        LANE_ASSOCIATION_KEY,
        ORIGIN_GROUP,
        ORIGIN_COUNTRY,
        ORIGIN_CITY_STATE,
        ORIGIN,
        ORIGIN_ZIP,
        DESTINATION_COUNTRY,
        DESTINATION_CITY_STATE,
        DESTINATION,
        COMMODITY,
        EQUIPMENT,
        TOTAL_MILES,
        BID_LOADS,
        UPDATED_LOADS,
        HISTORICAL_LOADS,
        FMIC,
        COMMENTS,
        PRIMARY_CUSTOMER,
        PRIMARY_UNLOAD_TYPE,
        ORDER_TYPE,
        EFFECTIVE_DATE,
        EXPIRATION_DATE,
        PRE_AWARDED_LANE,
        AAO,
        COSTCO_GROUP,
        AWARD_WEIGHTED_RPM,
        AWARD_WEIGHTED_SERVICE,
        COUPA_TAB,
        BUSINESS_UNIT,
        CURRENT_VOLUME,
        DESTINATION_ZIP,
        LTA,
        CC_SOURCE_SYSTEM,
        CC_CREATED_DATETIMESTAMP,
        CC_UPDATED_DATETIMESTAMP,
        CC_TIMEZONE
    FROM(
        --Cte: Transport Lane Rate Base Filtering
        WITH CTE_TLR AS 
        (
        SELECT CONCAT (UDMTLR.ORIGIN_ZONE_CODE, '-', UDMTLR.DESTINATION_ZONE_CODE) AS LANE,
            UDMTLR.ORIGIN_COUNTRY_CODE,
            UDMTLR.ORIGIN_ZONE_CODE,
            UDMTLR.DESTINATION_COUNTRY_CODE,
            UDMTLR.DESTINATION_ZONE_CODE,
            UDMTLR.LANE_ASSOCIATION_KEY,
            UDMTLR.RATE_EFFECTIVE_DATE AS RATE_EFFECTIVE_DATE,
            UDMTLR.RATE_EXPIRATION_DATE AS RATE_EXPIRATION_DATE
        FROM SC_LOG.INT_UDM_TMS.T_TD_GBL_SC_TMS_LOAD_TRANSPORT_LANE_RATE UDMTLR
        
        WHERE 
        --Filter: NA Division
        UDMTLR.BASE_DIVISION_KEY IN (SELECT DIVISION_KEY FROM SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_DIVISION WHERE UPPER(DIVISION_CODE) ='KCNA') 
        --Filter: Origin Zone Cleansing
        AND ( SUBSTR(UDMTLR.ORIGIN_ZONE_CODE, 1, 1) <> '9' 
        AND SUBSTR(UDMTLR.ORIGIN_ZONE_CODE, 1, 1) <> '5' 
        AND UDMTLR.ORIGIN_ZONE_CODE NOT IN ('ALLUSA', 'ALLMEX', 'ALLCAN') 
        AND UDMTLR.ORIGIN_ZONE_CODE NOT LIKE 'US-%' 
        AND UDMTLR.ORIGIN_ZONE_CODE NOT LIKE '5-%' 
        AND UDMTLR.ORIGIN_ZONE_CODE NOT LIKE 'ZAR-%'
        --Filter: Destination Zone Cleansing
        AND UDMTLR.ORIGIN_ZONE_CODE NOT LIKE 'REGION%' ) 
        AND ( SUBSTR(UDMTLR.DESTINATION_ZONE_CODE, 1, 1) <> '9' 
        AND UDMTLR.DESTINATION_ZONE_CODE NOT IN ('ALLUSA', 'ALLMEX', 'ALLCAN') 
        AND UDMTLR.DESTINATION_ZONE_CODE NOT LIKE 'US-%' 
        AND UDMTLR.DESTINATION_ZONE_CODE NOT LIKE '6%' 
        AND UDMTLR.DESTINATION_ZONE_CODE NOT LIKE 'REGION%' 
        AND UDMTLR.DESTINATION_ZONE_CODE NOT LIKE '5%' 
        AND UDMTLR.DESTINATION_ZONE_CODE NOT LIKE 'ALLPRI' 
        AND UDMTLR.DESTINATION_ZONE_CODE NOT LIKE 'US_3%' ) 
        --Filter: Active Rates Based On Current Date
        AND ( CAST(UDMTLR.RATE_EFFECTIVE_DATE AS DATE) <= CURRENT_DATE 
        AND UDMTLR.RATE_EXPIRATION_DATE >= CURRENT_DATE) 
        --Filter: Allowed Charge Codes
        AND UDMTLR.CHARGE_CODE IN ('MILE', 'ZTEM', 'TCM', 'NA_FLAT') 
        AND 
        --Filter: Origin And Destination Special Handling 
        ( 
            --ORIGIN ZONE CODE FILTERS  
            (UDMTLR.ORIGIN_ZONE_CODE NOT LIKE 'KC%' OR UDMTLR.ORIGIN_ZONE_CODE LIKE 'KCILROME%') 
            AND UDMTLR.ORIGIN_ZONE_CODE NOT LIKE '5%' 
            AND UDMTLR.ORIGIN_ZONE_CODE NOT LIKE 'ZAR%'
            
            -- DEST ZONE CODE FILTERS  
            AND (UDMTLR.DESTINATION_ZONE_CODE NOT LIKE 'KC%' OR UDMTLR.DESTINATION_ZONE_CODE LIKE 'KCILROME%') 
            AND UDMTLR.DESTINATION_ZONE_CODE NOT LIKE 'US_9%' 
            AND (LEFT(UDMTLR.DESTINATION_ZONE_CODE, 3) != 'US_'  
            OR TRY_TO_NUMBER(SUBSTRING(UDMTLR.DESTINATION_ZONE_CODE, 4, 1)) IS NOT NULL)
            
            -- SERVICE FILTERS 
            AND UDMTLR.SERVICE_CODE NOT LIKE 'ZAR%'
        )
        ),
        --Cte: Sales Org Priority Selection
        CTAS_SALE_ORG AS
        (
            SELECT
                LOAD_LEG_ID,
                TRIM(EXTENDED_ATTRIBUTES_TMS:SHPM_REFERENCE_NUMBER_22, '""""') AS SALES_ORG,
                ROW_NUMBER() OVER(PARTITION BY LOAD_LEG_ID ORDER BY SUM(BASE_VOLUME) DESC) AS ROW_NBR
            FROM SC_LOG.INT_UDM_TMS.T_TD_GBL_SC_TMS_LOAD_TRANSPORT_DETAIL
            GROUP BY LOAD_LEG_ID,EXTENDED_ATTRIBUTES_TMS:SHPM_REFERENCE_NUMBER_22
        QUALIFY ROW_NBR =1
        ),
        --Cte: Order Type Classification
        CTE_ORDER_TYPE AS
        (
            SELECT LT.LOAD_LEG_ID, 
            --Case: Order Type Derivation
            CASE WHEN UPPER(SO.SALES_ORG) = 'Z05' 
                        THEN 'NON WOVENS' 
                WHEN UPPER(LT.LOAD_AT_CORPORATE_ID_1) = 'RM'
                        THEN 'RM-INBOUND'
                WHEN UPPER(LT.LOAD_AT_CORPORATE_ID_1) = 'RF'
                        THEN 'RF-INBOUND'
                WHEN UPPER(SUBSTRING(LT.LAST_SHIPPING_LOCATION_CODE,1,1)) = 'R' 
                        THEN 'RETURNS'
                WHEN (SUBSTRING(LT.LAST_SHIPPING_LOCATION_CODE,1,1) = '1' OR SUBSTRING(LT.LAST_SHIPPING_LOCATION_CODE,1,1) = '2') 
                        THEN 'INTERMILL'
                WHEN (SUBSTRING(LT.LAST_SHIPPING_LOCATION_CODE,1,1) = '5' OR SUBSTRING(LT.LAST_SHIPPING_LOCATION_CODE,1,1) = '9' )
                        THEN 'CUSTOMER'         
            ELSE 'CUSTOMER' END AS ORDER_TYPE
            FROM SC_LOG.INT_UDM_TMS.T_TD_GBL_SC_TMS_LOAD_TRANSPORT LT 
            LEFT JOIN CTAS_SALE_ORG SO ON SO.LOAD_LEG_ID = LT.LOAD_LEG_ID
        ),
        --Cte: Load Transport Aggregation
        CTE_LD_TRANSPORT AS
        (
            SELECT DISTINCT 
                CONCAT(CTE_TLR.ORIGIN_ZONE_CODE, '-', CTE_TLR.DESTINATION_ZONE_CODE) AS LANE,
                CTE_TLR.ORIGIN_ZONE_CODE,
                CTE_TLR.DESTINATION_ZONE_CODE,
                ORIGIN_ZIP,
                DESTINATION_ZIP,
                AD_ORIG.CITY_NAME || ', ' || AD_ORIG.STATE_CODE AS ORIGIN,
                AD_DEST.CITY_NAME || ', ' || AD_DEST.STATE_CODE AS DESTINATION,
                MAX(DISTANCE_IN_MILE_TOTAL) as total_miles,
                ORDER_TYPE,
                COUNT(LT.LOAD_LEG_ID) AS LOAD_COUNT,
                ROW_NUMBER() OVER (PARTITION BY CONCAT(CTE_TLR.ORIGIN_ZONE_CODE, '-', CTE_TLR.DESTINATION_ZONE_CODE) ORDER BY LOAD_COUNT DESC) AS RN
            FROM 
            (
                SELECT 
                    CONCAT(LA.ORIGIN_ZONE_CODE, '-', LA.DESTINATION_ZONE_CODE) AS LANE,
                    LEFT(TRIM(LT.EXTENDED_ATTRIBUTES_TMS:ORIGIN_POSTAL_CODE,'"'),5) AS ORIGIN_ZIP,
                    LEFT(TRIM(LT.EXTENDED_ATTRIBUTES_TMS:DESTINATION_POSTTAL_CODE,'"'),5) AS DESTINATION_ZIP, 
                    LT.LOAD_LEG_ID,
                    OT.ORDER_TYPE,
                    LT.FIXED_ITINERARY_DISTANCE*0.621379 AS DISTANCE_IN_MILE_TOTAL --Conversion To Mile
                FROM SC_LOG.INT_UDM_TMS.T_TD_GBL_SC_TMS_LOAD_TRANSPORT LT
                LEFT JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_LANE_ASSOCIATION LA
                    ON LA.LANE_ASSOCIATION_KEY = LT.LANE_ASSOCIATION_KEY
                LEFT JOIN CTE_ORDER_TYPE OT ON OT.LOAD_LEG_ID=LT.LOAD_LEG_ID
                WHERE LT.FIXED_ITINERARY_DISTANCE*0.621379 >=2 --Filter: Minimum Distance Threshold 
            ) LT 
            LEFT JOIN CTE_TLR        
            ON LT.LANE=CTE_TLR.LANE
            LEFT JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_ADDRESS AD_ORIG ON LEFT(AD_ORIG.POSTAL_CODE,5) = ORIGIN_ZIP
            LEFT JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_ADDRESS AD_DEST ON LEFT(AD_DEST.POSTAL_CODE,5) = DESTINATION_ZIP
            WHERE 
                UPPER(AD_ORIG.COUNTRY_CODE) IN ('USA', 'MEX', 'CAN') AND UPPER(AD_DEST.COUNTRY_CODE) IN ('USA', 'MEX', 'CAN') --Filter: Valid Countries
                GROUP BY ALL
            QUALIFY RN = 1
        ),
        --Cte: Origin Zip Aggregation
        CTE_ORIGIN_RANKED AS 
        (
            SELECT 
                ORIGIN_ZONE_CODE,  
            ORIGIN_ZIP, 
                COUNT(ORIGIN_ZIP) AS CNT,
            ROW_NUMBER() over (PARTITION by ORIGIN_ZONE_CODE order by CNT desc) as rn 
            FROM CTE_LD_TRANSPORT
            WHERE ORIGIN_ZIP is not null
            GROUP BY ORIGIN_ZONE_CODE,ORIGIN_ZIP
        ),
        CTE_ORIGIN_ZIP AS
        (
            SELECT 
                ORIGIN_ZONE_CODE,  
                UPPER(LISTAGG(ORIGIN_ZIP, ', ') WITHIN GROUP (ORDER BY CNT DESC)) AS ORIGIN_ZIP
            FROM CTE_ORIGIN_RANKED
            WHERE RN <= 3 
            GROUP BY ORIGIN_ZONE_CODE
        ),
        --Cte: Destination Zip Aggregation
        CTE_DEST_RANKED as (
            SELECT 
                DESTINATION_ZONE_CODE,  
                --Case: Fallback For US_5 Zones
                CASE WHEN DESTINATION_ZONE_CODE LIKE 'US_5%' AND DESTINATION_ZIP IS NULL THEN RIGHT(DESTINATION_ZONE_CODE,5)
                ELSE DESTINATION_ZIP END AS DESTINATION_ZIP, 
                COUNT(DESTINATION_ZIP) AS CNT,
                ROW_NUMBER() over (PARTITION by DESTINATION_ZONE_CODE order by CNT desc) as rn 
            FROM CTE_LD_TRANSPORT
            WHERE DESTINATION_ZIP is not null
            GROUP BY DESTINATION_ZONE_CODE,DESTINATION_ZIP
        ),
        CTE_DESTINATION_ZIP AS
        (
            SELECT 
                DESTINATION_ZONE_CODE,  
                UPPER(LISTAGG(DESTINATION_ZIP, ', ') WITHIN GROUP (ORDER BY CNT DESC)) AS DESTINATION_ZIP
            FROM CTE_DEST_RANKED
            WHERE RN <= 3 
            GROUP BY DESTINATION_ZONE_CODE
        ),
        --Cte: Base For Primary Customer
        AGGREGATEDDATA AS (
            SELECT  
                LT.LANE_ASSOCIATION_KEY, 
                CONCAT (LA.ORIGIN_ZONE_CODE, '-', LA.DESTINATION_ZONE_CODE) AS LANE,
                CFG.CODE_DESCRIPTION AS CUSTOMER_HIERARCHY, 
                COUNT(DISTINCT LT.LOAD_LEG_ID) AS DISTINCTLEGCOUNT 
            FROM SC_LOG.INT_UDM_TMS.T_TD_GBL_SC_TMS_LOAD_TRANSPORT LT
            LEFT JOIN TMS.STAGING.T_GBL_BY_CONFIG_LKP CFG  
                ON CFG.COLUMN_NAME = 'HIERARCHY'  
                AND CFG.CODE_VALUE = SUBSTR(LT.LAST_SHIPPING_LOCATION_CODE, 1, 8) 
            INNER JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_LANE_ASSOCIATION LA
                ON LA.LANE_ASSOCIATION_KEY = LT.LANE_ASSOCIATION_KEY 
            WHERE LT.TRAILER_EQUIPMENT_TYPE <> 'LTL' 
            AND COALESCE(LT.SHIPPED_DATE,TRIM(LT.EXTENDED_ATTRIBUTES_TMS:START_DATE_TIME,'"'),LT.SHIPMENT_CREATED_DATE_TIME) 
        >= '2026-03-15'
        and UPPER(CURRENT_OPERATIONAL_STATUS_DESCRIPTION) not in ('LL CLOSED','LL CANCELLED', 'LL DELETE' )
            GROUP BY    
                CONCAT (LA.ORIGIN_ZONE_CODE, '-', LA.DESTINATION_ZONE_CODE), 
                CUSTOMER_HIERARCHY,
                LT.LANE_ASSOCIATION_KEY
        ), 
        --Cte: Ranking For Primary Customer
        RANKEDDATA AS 
        (
            SELECT
                LANE_ASSOCIATION_KEY,
                LANE,
                CUSTOMER_HIERARCHY,
                DISTINCTLEGCOUNT, --volume
                ROW_NUMBER() OVER (
                    PARTITION BY LANE  
                    ORDER BY DISTINCTLEGCOUNT DESC, CUSTOMER_HIERARCHY ASC 
                ) AS ROWNUM 
            FROM AGGREGATEDDATA 
        ), 
        --Cte: Final Calculation For Primary Customer
        PRIMARY_CUSTOMER_TBL AS
        (
        SELECT  
            LANE_ASSOCIATION_KEY AS LANE_ASSOCIATION_KEY,
            LANE,
            MAX(DISTINCTLEGCOUNT) AS CURRENT_VOLUME,
            UPPER(LISTAGG(CUSTOMER_HIERARCHY, ', ') 
                WITHIN GROUP (ORDER BY DISTINCTLEGCOUNT DESC, CUSTOMER_HIERARCHY ASC)) AS PRIMARY_CUSTOMERS 
        FROM RANKEDDATA 
        WHERE ROWNUM <= 5 
        GROUP BY 
            LANE,
            LANE_ASSOCIATION_KEY
        )
        --Cte: Updated Loads Aggregation
        ,CTE_UPDATED_LOADS AS (
        SELECT 
            UDMTLR.LANE as LANE,
            SUM(MAX_QUANTITY_CONSTRAINT*52) AS UPDATED_LOADS
            FROM 
            CTE_TLR UDMTLR 
                LEFT JOIN SC_LOG.INT_TMS.V_TD_NA_SC_TMS_QUANTITY_CONSTRAINT QC 
                ON CONCAT(UDMTLR.ORIGIN_ZONE_CODE,'-',UDMTLR.DESTINATION_ZONE_CODE) = QC.LANE_DESCRIPTION
                AND QC.RATE_EFFECTIVE_DATE = UDMTLR.RATE_EFFECTIVE_DATE
                AND QC.RATE_EXPIRATION_DATE = UDMTLR.RATE_EXPIRATION_DATE
            GROUP BY UDMTLR.LANE
        ) 
        --Final Select
        SELECT 
        UDMTLR.LANE,
        UDMTLR.LANE_ASSOCIATION_KEY,
        CLK.CODE_DESCRIPTION AS ORIGIN_GROUP,
        UDMTLR.ORIGIN_COUNTRY_CODE AS ORIGIN_COUNTRY,
        UDMTLR.ORIGIN_ZONE_CODE AS ORIGIN_CITY_STATE,
        --Case: Special Origin Mapping
        CASE WHEN UPPER(UDMTLR.ORIGIN_ZONE_CODE) IN ('BILCAMP','NCEFLROC','WIFOXVAL','WIGBCAMP','WIMILCAM') 
            THEN CLK.CODE_DESCRIPTION 
            ELSE CLT.ORIGIN END AS ORIGIN,
        OZ.ORIGIN_ZIP AS ORIGIN_ZIP,
        UDMTLR.DESTINATION_COUNTRY_CODE AS DESTINATION_COUNTRY,
        UDMTLR.DESTINATION_ZONE_CODE AS DESTINATION_CITY_STATE,
        CLT.DESTINATION AS DESTINATION,
        NULL AS COMMODITY,
        'D' AS EQUIPMENT,--Hardcoded Equipment
        COALESCE(CLT.TOTAL_MILES,1) AS TOTAL_MILES,
        NULL AS BID_LOADS,
        UPDATED_LOADS,
        NULL AS HISTORICAL_LOADS,
        NULL AS FMIC,
        NULL AS COMMENTS,
        PRCUS.PRIMARY_CUSTOMERS AS PRIMARY_CUSTOMER,
        NULL AS PRIMARY_UNLOAD_TYPE,
        CLT.ORDER_TYPE AS ORDER_TYPE,
        '2026-03-15' AS EFFECTIVE_DATE, --Start Of The Year- To Be Moved To Config
        '2027-03-13' AS EXPIRATION_DATE, --End Of The Year- To Be Moved To Config
        NULL AS PRE_AWARDED_LANE,
        NULL AS AAO, 
        NULL AS COSTCO_GROUP,
        NULL AS AWARD_WEIGHTED_RPM, --UPGRADE FROM BAR
        NULL AS AWARD_WEIGHTED_SERVICE, --UPGRADE FROM BAR
        NULL AS COUPA_TAB,
        NULL AS BUSINESS_UNIT,
        PRCUS.CURRENT_VOLUME AS CURRENT_VOLUME,
        CASE WHEN UDMTLR.DESTINATION_ZONE_CODE LIKE 'US_5%' AND DZ.DESTINATION_ZIP IS NULL THEN RIGHT(UDMTLR.DESTINATION_ZONE_CODE,5) 
        ELSE DZ.DESTINATION_ZIP END AS DESTINATION_ZIP,
        'N' AS LTA,
        'BY' AS CC_SOURCE_SYSTEM,
        CONVERT_TIMEZONE('America/Los_Angeles','UTC', CURRENT_TIMESTAMP())::TIMESTAMP_NTZ(9) AS CC_CREATED_DATETIMESTAMP,
        CONVERT_TIMEZONE('America/Los_Angeles','UTC', CURRENT_TIMESTAMP())::TIMESTAMP_NTZ(9) AS CC_UPDATED_DATETIMESTAMP,
        'UTC' AS CC_TIMEZONE,
        ROW_NUMBER() OVER (PARTITION BY UDMTLR.LANE ORDER BY PRCUS.CURRENT_VOLUME DESC NULLS LAST, CLT.TOTAL_MILES DESC NULLS LAST
        ) AS RW_NUMB
    
    
        FROM CTE_TLR UDMTLR 
        LEFT JOIN CTE_LD_TRANSPORT CLT
        ON CLT.LANE = UDMTLR.LANE 
        LEFT JOIN CTE_ORIGIN_ZIP OZ
        ON UDMTLR.ORIGIN_ZONE_CODE=OZ.ORIGIN_ZONE_CODE
        LEFT JOIN CTE_DESTINATION_ZIP DZ
        ON UDMTLR.DESTINATION_ZONE_CODE=DZ.DESTINATION_ZONE_CODE
        
        LEFT JOIN TMS.STAGING.T_GBL_BY_CONFIG_LKP CLK ON CLK.COLUMN_NAME = 'ORIGIN' AND CLK.CODE_VALUE = UDMTLR.ORIGIN_ZONE_CODE
        
        INNER JOIN CTE_UPDATED_LOADS QC ON
        CONCAT(UDMTLR.ORIGIN_ZONE_CODE,'-',UDMTLR.DESTINATION_ZONE_CODE) = QC.LANE
        
        LEFT JOIN PRIMARY_CUSTOMER_TBL PRCUS
        ON PRCUS.LANE=CONCAT(UDMTLR.ORIGIN_ZONE_CODE, '-', UDMTLR.DESTINATION_ZONE_CODE) 
        
        
        GROUP BY 
            UDMTLR.LANE,
            UDMTLR.LANE_ASSOCIATION_KEY,
            CLK.CODE_DESCRIPTION,
            UDMTLR.ORIGIN_COUNTRY_CODE,
            UDMTLR.ORIGIN_ZONE_CODE,
            CASE WHEN UDMTLR.ORIGIN_ZONE_CODE IN ('BILCAMP','NCEFLROC','WIFOXVAL','WIGBCAMP','WIMILCAM') 
                    THEN CLK.CODE_DESCRIPTION 
                    ELSE CLT.ORIGIN END,
            OZ.ORIGIN_ZIP,
            UDMTLR.DESTINATION_COUNTRY_CODE,
            UDMTLR.DESTINATION_ZONE_CODE,
            CLT.DESTINATION,
            CLT.TOTAL_MILES,
            UPDATED_LOADS,
            PRIMARY_CUSTOMERS,
            CLT.ORDER_TYPE,
            CURRENT_VOLUME,
            DZ.DESTINATION_ZIP
        QUALIFY RW_NUMB=1
    )
);
