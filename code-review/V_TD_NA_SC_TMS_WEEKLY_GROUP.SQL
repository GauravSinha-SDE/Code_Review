CREATE OR REPLACE VIEW SC_LOG.INT_TMS.V_TD_NA_SC_TMS_WEEKLY_GROUP(
    WEEKLY_GROUP_KEY COMMENT 'Primary Key | Composite Of TRANSPORT_LANE_RATE_KEY And WEEK_GROUP',
    CARRIER_KEY COMMENT 'Foreign Key From T_MD_GBL_SC_TMS_CARRIER.CARRIER_KEY Table',
    MASTER_SERVICE_KEY COMMENT 'Foreign Key From T_MD_GBL_SC_TMS_SERVICE.MASTER_SERVICE_KEY Table',
    TARIFF_KEY COMMENT 'Foreign Key From T_MD_GBL_SC_TMS_TARIFF.TARIFF_KEY Table',
    CARRIER_CODE COMMENT 'Standardized Carrier Identifier',
    SERVICE_CODE COMMENT 'Service Id',
    TARIFF_CODE COMMENT 'Tariff Id',
    ORIGIN_ZONE COMMENT 'Origin Geographic Zone Code',
    DESTINATION_ZONE COMMENT 'Destination Geographic Zone Code',
    EQUIP_TYPE COMMENT 'Equipment Category The Rate Applies To',
    LANE_DESCRIPTION COMMENT 'Lane Description',
    RATE COMMENT 'Rate',
    LANE_EFFECTIVE_DATE COMMENT 'Lane Validity Start Date-Time',
    LANE_EXPIRATION_DATE COMMENT 'Lane Validity End Date-Time',
    TOTAL_MAX_QTY_CONSTRAINT COMMENT 'Maximum Allocation Or Quantity Cap',
    WEEK_GROUP COMMENT 'Week Group',
    CC_SOURCE_SYSTEM COMMENT 'Source System',
    CC_CREATED_DATETIMESTAMP COMMENT 'Record Created Time Stamp',
    CC_UPDATED_DATETIMESTAMP COMMENT 'Record Updated Time Stamp',
    CC_TIMEZONE COMMENT 'Time Zone'
) COMMENT='Weekly Group View To Pull New Carrier Lane Daily Constraints'
as(
--Cte: Daily Quantity Constraint For Each Carrier In Every Lane
WITH TEMPQTY_DAILY AS(
    SELECT DISTINCT 
        TAR.CARRIER_KEY AS CARRIER_KEY,
        LA.MASTER_SERVICE_KEY AS MASTER_SERVICE_KEY,
        TAR.TARIFF_KEY AS TARIFF_KEY,
        LA.LANE_ASSOCIATION_KEY AS LANE_ASSOCIATION_KEY,
        CAR.CARRIER_ID AS CARRIER_CODE,
        SER.SERVICE_CODE AS SERVICE_CODE,
        TAR.TARIFF_ID AS TARIFF_ID,
        TAR.TARIFF_CODE AS TARIFF_CODE,
        LA.ORIGIN_ZONE_CODE AS ORIGIN_ZONE,
        LA.DESTINATION_ZONE_CODE AS DESTINATION_ZONE,
        TS.EQUIPMENT_TYPE_CODE AS EQUIP_TYPE,
        RT.RATE_CODE AS LANE_DESCRIPTION,
        RT.CHARGE_CODE AS CHARGE_CODE,
        RR.BREAK_RATE_AMOUNT AS RATE,
        LAV.START_DATE_TIME AS LANE_EFFECTIVE_DATE,
        LAV.END_DATE_TIME AS LANE_EXPIRATION_DATE,
        RT.EFFECTIVE_DATE AS RATE_EFFECTIVE_DATE,
        RT.EXPIRATION_DATE AS RATE_EXPIRATION_DATE,
        --Business Logic To Get Quantity Constraint
        CASE WHEN LAV.MAXIMUM_LOADS = 999 AND LAV.MINIMUM_LOADS <> 0
			THEN LAV.MINIMUM_LOADS
			ELSE LAV.MAXIMUM_LOADS
			END AS MAX_QUANTITY_CONSTRAINT,
        'BY' AS CC_SOURCE_SYSTEM,
        CONVERT_TIMEZONE('America/Los_Angeles','UTC',CURRENT_TIMESTAMP()) AS CC_CREATED_DATETIMESTAMP,
        CONVERT_TIMEZONE('America/Los_Angeles','UTC',CURRENT_TIMESTAMP()) AS CC_UPDATED_DATETIMESTAMP,
        'UTC' AS CC_TIMEZONE
FROM SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_TARIFF TAR
INNER JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_CARRIER CAR 
    ON TAR.CARRIER_KEY = CAR.CARRIER_KEY
INNER JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_LANE_ASSOCIATION LA
    ON TAR.TARIFF_KEY = LA.TARIFF_KEY
INNER JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_TARIFF_SERVICES TS
    ON TS.TARIFF_KEY = LA.TARIFF_KEY 
    AND TS.MASTER_SERVICE_KEY = LA.MASTER_SERVICE_KEY
INNER JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_RATE RT 
    ON RT.TARIFF_KEY = TS.TARIFF_KEY
    AND RT.MASTER_SERVICE_KEY = TS.MASTER_SERVICE_KEY
    AND RT.CHARGE_CODE = TS.CHARGE_CODE 
    AND RT.RATE_CODE_KEY = LA.RATE_CODE_KEY
INNER JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_RANGE_RATE RR 
    ON RR.RATE_KEY = RT.RATE_KEY
INNER JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_LANE_AVAILABILITY LAV 
    ON LA.TARIFF_KEY = LAV.TARIFF_KEY
    AND LA.ORIGIN_ZONE_CODE = LAV.ORIGIN_ZONE_CODE
    AND LA.DESTINATION_ZONE_CODE = LAV.DESTINATION_ZONE_CODE
LEFT JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_DIVISION DIV 
    ON TAR.DIVISION_KEY = DIV.DIVISION_KEY
LEFT JOIN SC_MD.INT_SC_MD.T_MD_GBL_SC_TMS_SERVICE SER 
    ON LA.MASTER_SERVICE_KEY = SER.MASTER_SERVICE_KEY
WHERE 
    UPPER(DIVISION_CODE) = 'KCNA' --Filter For NA Specific Data
    AND UPPER(TAR.TARIFF_STATUS_DESCRIPTION) = 'ACTIVE' --Filter For Active Tariff
    AND RATE_EFFECTIVE_DATE >= '2025-01-01' --Filter For Data From 2025 Onwards
    AND TO_DATE(RT.EXPIRATION_DATE) > TO_DATE(CURRENT_TIMESTAMP())
    AND UPPER(RT.CHARGE_CODE) IN ('NA_FLAT','MILE')  --Filter For Charge Code
    AND UPPER(TRIM(LAV.CONSTRAINT_PERCENT_FLAG)) = 'F' --Filter Needed For Quantity Constraints
    AND UPPER(LAV.BUCKET_TYPE_DESCRIPTION) = 'DAILY' --Filter Needed For Daily Constraints
),
FIRSTSUNDAYS AS (
SELECT 
LANE_DESCRIPTION, 
CARRIER_CODE, 
SERVICE_CODE, 
MIN(LANE_EFFECTIVE_DATE) AS FIRSTSUNDAY 
FROM TEMPQTY_DAILY QTYC 
WHERE UPPER(DAYNAME(LANE_EFFECTIVE_DATE)) = 'SUN' --To Calculate First Sundays
GROUP BY LANE_DESCRIPTION,CARRIER_CODE,SERVICE_CODE 
),
--Ranking According Latest Lane Effective Date
RANKEDDATES AS ( 
SELECT
    LR.CARRIER_KEY,
    LR.MASTER_SERVICE_KEY,
    LR.TARIFF_KEY,
    LR.CARRIER_CODE,
    LR.SERVICE_CODE,
    LR.TARIFF_CODE,
    LR.ORIGIN_ZONE,
    LR.DESTINATION_ZONE,
    LR.EQUIP_TYPE,
    LR.LANE_DESCRIPTION,
    LR.RATE,
    LR.LANE_EFFECTIVE_DATE,
    LR.MAX_QUANTITY_CONSTRAINT,
ROW_NUMBER() OVER ( 
PARTITION BY LR.LANE_DESCRIPTION, LR.CARRIER_CODE, LR.SERVICE_CODE 
ORDER BY LR.LANE_EFFECTIVE_DATE ) AS RN 
FROM TEMPQTY_DAILY LR 
INNER JOIN FIRSTSUNDAYS FS 
ON LR.LANE_DESCRIPTION = FS.LANE_DESCRIPTION 
AND LR.CARRIER_CODE = FS.CARRIER_CODE 
AND LR.SERVICE_CODE = FS.SERVICE_CODE 
WHERE LR.LANE_EFFECTIVE_DATE >= FS.FIRSTSUNDAY
),
--Filter To Get The Row With Latest Lane Effective Date 
FILTERED AS ( 
SELECT *
FROM (
  SELECT
    RD.*,
    FLOOR(COUNT(*) OVER (
      PARTITION BY LANE_DESCRIPTION, CARRIER_CODE, SERVICE_CODE
    ) / 7) * 7 AS KEEP_UPTO --Calculates Largest Multiple Of 7 Rows To Retain Per Lane Carrier And Service Group
  FROM RankedDates RD
)
WHERE RN <= KEEP_UPTO --Filter: Retain Only Complete Weeks
)
--Final Select Statement
SELECT 
    CONCAT(CARRIER_CODE,SERVICE_CODE,LANE_DESCRIPTION,MIN(LANE_EFFECTIVE_DATE)) AS WEEKLY_GROUP_KEY,
    CARRIER_KEY,
    MASTER_SERVICE_KEY,
    TARIFF_KEY,
    CARRIER_CODE,
    SERVICE_CODE,
    TARIFF_CODE,
    ORIGIN_ZONE,
    DESTINATION_ZONE,
    EQUIP_TYPE,
    LANE_DESCRIPTION,
    AVG(RATE) AS RATE,
    MIN(LANE_EFFECTIVE_DATE) AS LANE_EFFECTIVE_DATE,
    DATEADD(SECOND, -1, DATEADD(DAY, 1, MAX(LANE_EFFECTIVE_DATE))) AS LANE_EXPIRATION_DATE, --Lane Expiration As End Of Week
    SUM(MAX_QUANTITY_CONSTRAINT) AS TOTAL_MAX_QTY_CONSTRAINT,
    FLOOR((RN - 1) / 7) AS WEEK_GROUP, --Weekly Group Number
    'BY' AS CC_SOURCE_SYSTEM,
    CONVERT_TIMEZONE('America/Los_Angeles','UTC', CURRENT_TIMESTAMP())::TIMESTAMP_NTZ(9) AS CC_CREATED_DATETIMESTAMP,
    CONVERT_TIMEZONE('America/Los_Angeles','UTC', CURRENT_TIMESTAMP())::TIMESTAMP_NTZ(9) AS CC_UPDATED_DATETIMESTAMP,
    'UTC' AS CC_TIMEZONE
FROM FILTERED
GROUP BY ALL
);
